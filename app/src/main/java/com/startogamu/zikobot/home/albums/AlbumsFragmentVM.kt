class AlbumsFragmentVM(fragment: AlbumsFragment, binding: AlbumsFragmentBinding, savedInstance: Bundle?) :
        FragmentRecyclerBaseVM<AlbumVM, AlbumsFragment, AlbumsFragmentBinding>(fragment, binding, savedInstance) {

    lateinit var loading: ObservableBoolean
    private var disposable: Disposable? = null
    val adapter = AlphabeticalAdapter<AlbumVM>()

    override fun itemLayoutResource(): Int {
        return R.layout.album_item
    }

    override fun itemData(): Int {
        return BR.albumVM
    }

    override fun onCreate(savedInstance: Bundle?) {
        super.onCreate(savedInstance)
        loading = ObservableBoolean(true)
        disposable = LocalMusicManager.INSTANCE.synchroDone
                .subscribeOn(Schedulers.io())
                .observeOn(AndroidSchedulers.mainThread())
                .subscribe(this::updateList)
        binding.fastScroller.setRecyclerView(binding.albumsFragmentRV)

    }

    override fun onDestroy() {
        if (disposable != null)
            disposable?.dispose()
        super.onDestroy()
    }

    private fun updateList(done: Boolean) {
        if (!done)
            return
        items.clear()
        handleAlbumAlphabet()

        AlbumManager.findAll()
                .subscribe({
                    loading.set(false)
                    for (album in it) {
                        items.add(AlbumVM(false, fragment.context, album))
                    }
                }, {
                    loading.set(false)
                    AppLog.INSTANCE.e("albums", it.localizedMessage);
                })
    }
    private fun handleAlbumAlphabet() {
        AlbumManager.findAll()
                .subscribe({
                    AppUtils.setupAlphabet(binding.fastScroller,it.map { it.name }.toMutableList())
                }, {
                    AppLog.INSTANCE.e("Artists", it.localizedMessage)
                })
    }
    companion object {
        private val TAG = AlbumsFragmentVM::class.java.simpleName
    }
}
